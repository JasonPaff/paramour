import { existsSync, readFileSync, statSync } from "node:fs";
import { join } from "node:path";
import { setTimeout as sleep } from "node:timers/promises";
import { describe, expect, it } from "vitest";

import { emitArtifact, writeIfChanged } from "../src";
import { makeTempDir } from "./helpers.js";

describe("emitArtifact (TR3)", () => {
  it("emits the exact artifact bytes", () => {
    const expected = `// Generated by @paramour-js/next. Do not edit — regenerate with \`paramour generate\`.
import "paramour";

declare module "paramour" {
  interface ParamourRegister {
    appRoutes:
      | "/"
      | "/about"
      | "/product/[id]";
  }
}
`;
    expect(emitArtifact(["/", "/about", "/product/[id]"])).toBe(expected);
  });

  it("keeps the leading-pipe union form for a single path", () => {
    expect(emitArtifact(["/"])).toContain('appRoutes:\n      | "/";');
  });

  it("is deterministic and sorts/dedupes its own input", () => {
    const canonical = emitArtifact(["/a", "/b"]);
    expect(emitArtifact(["/a", "/b"])).toBe(canonical);
    expect(emitArtifact(["/b", "/a", "/a"])).toBe(canonical);
  });

  it("uses LF endings and one trailing newline on every OS", () => {
    const content = emitArtifact(["/a"]);
    expect(content).not.toContain("\r");
    expect(content.endsWith("\n")).toBe(true);
    expect(content.endsWith("\n\n")).toBe(false);
  });

  it("sorts by code unit, not locale ('/Z' < '/a' < '/é')", () => {
    // A localeCompare regression would pass every ASCII-lowercase-only test;
    // this pin feeds TR3's byte-identical-on-every-OS guarantee.
    expect(emitArtifact(["/a", "/Z", "/é"])).toContain(
      ['      | "/Z"', '      | "/a"', '      | "/é";'].join("\n"),
    );
  });

  it("omits the appRoutes member entirely for an empty scan", () => {
    const content = emitArtifact([]);
    expect(content).toContain("interface ParamourRegister {}");
    expect(content).not.toContain("Routes:");
    expect(content).not.toContain("never");
  });
});

describe("writeIfChanged (TR3)", () => {
  it("creates a missing file and reports it wrote", () => {
    const file = join(makeTempDir(), "paramour-env.d.ts");
    expect(writeIfChanged(file, "content\n")).toEqual({
      previousContent: null,
      written: true,
    });
    expect(readFileSync(file, "utf8")).toBe("content\n");
  });

  it("leaves an identical file untouched", async () => {
    const file = join(makeTempDir(), "paramour-env.d.ts");
    writeIfChanged(file, "content\n");
    // The sleep guarantees a real rewrite WOULD have bumped mtime.
    await sleep(25);
    const mtimeBefore = statSync(file).mtimeMs;
    expect(writeIfChanged(file, "content\n")).toEqual({
      previousContent: "content\n",
      written: false,
    });
    expect(statSync(file).mtimeMs).toBe(mtimeBefore);
  });

  it("rewrites a changed file and returns the prior content", () => {
    const file = join(makeTempDir(), "paramour-env.d.ts");
    writeIfChanged(file, "old\n");
    expect(writeIfChanged(file, "new\n")).toEqual({
      previousContent: "old\n",
      written: true,
    });
    expect(readFileSync(file, "utf8")).toBe("new\n");
  });

  it("creates missing nested parent directories for the target file", () => {
    const file = join(makeTempDir(), "types", "deep", "routes.d.ts");
    expect(writeIfChanged(file, "content\n")).toEqual({
      previousContent: null,
      written: true,
    });
    expect(readFileSync(file, "utf8")).toBe("content\n");
  });

  it("distinguishes an empty existing file from a missing one", () => {
    const file = join(makeTempDir(), "paramour-env.d.ts");
    writeIfChanged(file, "");
    expect(existsSync(file)).toBe(true);
    expect(writeIfChanged(file, "")).toEqual({
      previousContent: "",
      written: false,
    });
  });
});
